/**
 * Build dist/nr-chat.global.js â€” the single-file `NrChat.*` artifact for GAS,
 * userscripts and anywhere else without module support.
 *
 * No bundler: the library is three files with relative imports, so concatenating
 * them into one IIFE scope resolves every binding. Anything this script does not
 * recognize is a hard error â€” a silently mis-stripped `export` would ship a
 * broken global.
 */

import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const dist = path.join(root, 'dist')

/** Dependency order: a module may only import from ones already emitted. */
const MODULES = ['json5.js', 'format.js']
const ENTRY = 'index.js'

const RE_IMPORT = /^import\s*\{([^}]*)\}\s*from\s*'\.\/([\w.-]+)';?\s*$/
const RE_REEXPORT = /^export\s*\{([^}]*)\}\s*from\s*'\.\/([\w.-]+)';?\s*$/
const RE_EXPORT_DECL = /^export\s+(?:async\s+)?(?:function|const|class|let|var)\s+([\w$]+)/

const names = (list) => list.split(',').map((s) => s.trim()).filter(Boolean)

const read = (file) => {
  const full = path.join(dist, file)
  if (!fs.existsSync(full)) {
    throw new Error(`build/global: ${full} is missing â€” run tsc first`)
  }
  return fs.readFileSync(full, 'utf8')
}

/** file -> names it exports, so imports can be checked against real declarations. */
const declaredBy = new Map()
const bodies = []

for (const file of MODULES) {
  const out = []
  const declared = new Set()

  for (const line of read(file).split('\n')) {
    const imported = RE_IMPORT.exec(line)
    if (imported) {
      const [, list, from] = imported
      const source = declaredBy.get(from)
      if (!source) {
        throw new Error(`build/global: ${file} imports from ${from}, which is not emitted before it â€” fix MODULES order`)
      }
      for (const name of names(list)) {
        if (!source.has(name)) {
          throw new Error(`build/global: ${file} imports ${name}, which ${from} does not export`)
        }
      }
      continue
    }

    const exported = RE_EXPORT_DECL.exec(line)
    if (exported) {
      declared.add(exported[1])
      out.push(line.replace(/^export\s+/, ''))
      continue
    }

    if (/^(export|import)\b/.test(line)) {
      throw new Error(`build/global: unhandled module syntax in ${file}:\n  ${line}`)
    }

    out.push(line)
  }

  declaredBy.set(file, declared)
  bodies.push(`  // ---- ${file} ----\n` + out.map((l) => (l ? '  ' + l : l)).join('\n'))
}

// The entry decides the public surface; type-only exports are already erased.
const publicNames = []
for (const line of read(ENTRY).split('\n')) {
  if (line.trim() === '') continue

  const reexport = RE_REEXPORT.exec(line)
  if (!reexport) {
    throw new Error(`build/global: unexpected line in ${ENTRY}, expected only re-exports:\n  ${line}`)
  }

  const [, list, from] = reexport
  const source = declaredBy.get(from)
  if (!source) {
    throw new Error(`build/global: ${ENTRY} re-exports from ${from}, which is not in MODULES`)
  }
  for (const name of names(list)) {
    if (!source.has(name)) {
      throw new Error(`build/global: ${ENTRY} re-exports ${name}, which ${from} does not export`)
    }
  }
  publicNames.push(...names(list))
}

if (publicNames.length === 0) {
  throw new Error(`build/global: ${ENTRY} exposes nothing`)
}

const pkg = JSON.parse(fs.readFileSync(path.join(root, 'package.json'), 'utf8'))

const output = `/**
 * ${pkg.name} ${pkg.version} â€” ${pkg.description}
 * ${pkg.license} licensed. Generated by build/global.mjs; do not edit.
 *
 * Exposes a single global: NrChat.{${publicNames.join(', ')}}
 */
var NrChat = (function () {
  'use strict';

${bodies.join('\n\n')}

  return { ${publicNames.join(', ')} };
})();

if (typeof globalThis !== 'undefined') { globalThis.NrChat = NrChat; }
`

const target = path.join(dist, 'nr-chat.global.js')
fs.writeFileSync(target, output)

console.log(`build/global: ${path.relative(root, target)} â€” ${publicNames.length} exports, ${output.length} bytes`)
